package basic_things;

public class variableNaming {
    static void main(String[] args) {
        // we can start a variable name with $ or _ , rest of the special characters we cant use
        int xy = 3;
        int name = 4;
        int _x = 23;
        int $y = 12;
        System.out.println(xy); //3
        System.out.println(name); //4
        System.out.println(_x); //23
        System.out.println($y); //12
    }
}
