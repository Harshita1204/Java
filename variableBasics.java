package basic_things;

public class variableBasics {
    static void main(String[] args) {
        int x= 5; 
        System.out.println(x); //5
        System.out.println(x+7); //12
        int y= 20; 
        System.out.println(y); //20
        System.out.println(x+y); //25
        System.out.println(x*y); //100
    }
}
